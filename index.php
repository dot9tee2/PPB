        // Property Inquiry Form Submission
        function sendPropertyInquiry(e) {
            e.preventDefault();

            // Get the submit button
            const submitButton = document.getElementById('submitInquiry');
            const originalText = submitButton.innerHTML;
            
            // Disable the submit button and show loading state
            submitButton.disabled = true;
            submitButton.innerHTML = '<i class="fas fa-spinner fa-spin"></i> Sending...';

            // Get form data
            const formData = {
                name: document.getElementById('name').value,
                email: document.getElementById('email').value,
                phone: document.getElementById('phone').value,
                city: document.getElementById('city').value,
                propertyType: document.getElementById('propertyType').value,
                message: document.getElementById('message').value
            };

            // Send email using EmailJS
            emailjs.send(
                'YOUR_SERVICE_ID', // Replace with your EmailJS service ID
                'YOUR_TEMPLATE_ID', // Replace with your EmailJS template ID
                formData
            ).then(function(response) {
                console.log('SUCCESS!', response.status, response.text);
                
                // Show success notification
                showNotification('success', 'Your inquiry has been sent successfully! We will contact you soon.');
                
                // Reset form
                document.getElementById('propertyInquiryForm').reset();
                
                // Reset button state
                submitButton.disabled = false;
                submitButton.innerHTML = originalText;
                
            }, function(error) {
                console.log('FAILED...', error);
                
                // Show error notification
                showNotification('error', 'Sorry, there was an error sending your inquiry. Please try again later.');
                
                // Reset button state
                submitButton.disabled = false;
                submitButton.innerHTML = originalText;
            });

            return false;
        }

        // Function to show notifications
        function showNotification(type, message) {
            // Create notification element
            const notification = document.createElement('div');
            notification.className = `notification ${type}`;
            
            notification.innerHTML = `
                <div class="notification-content">
                    <div class="notification-icon">
                        <i class="fas ${type === 'success' ? 'fa-check-circle' : 'fa-exclamation-circle'}"></i>
                    </div>
                    <div class="notification-message">
                        ${message}
                    </div>
                    <button class="notification-close"><i class="fas fa-times"></i></button>
                </div>
            `;
            
            // Add to document
            document.body.appendChild(notification);
            
            // Add close button functionality
            const closeBtn = notification.querySelector('.notification-close');
            closeBtn.addEventListener('click', function() {
                notification.style.animation = 'slideOut 0.5s ease forwards';
                setTimeout(() => {
                    notification.remove();
                }, 500);
            });
            
